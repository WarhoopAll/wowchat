func ChannelID(name string) int {
	prefix := name
	for i := 0; i < len(name); i++ {
		if name[i] == ' ' {
			prefix = name[:i]
			break
		}
	}
	lower := make([]byte, len(prefix))
	for i := 0; i < len(prefix); i++ {
		c := prefix[i]
		if c >= 'A' && c <= 'Z' {
			c += 'a' - 'A'
		}
		lower[i] = c
	}
	switch string(lower) {
	case "general":
		return 0x01
	case "trade":
		return 0x02
	case "localdefense":
		return 0x16
	case "worlddefense":
		return 0x17
	case "guildrecruitment":
		return 0x19
	case "lookingforgroup":
		return 0x1A
	default:
		return 0x00
	}
}

