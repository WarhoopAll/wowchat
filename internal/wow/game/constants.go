const (
	raceOrc      byte = 0x02
	raceUndead   byte = 0x05
	raceTauren   byte = 0x06
	raceTroll    byte = 0x08
	raceGoblin   byte = 0x09
	raceBloodElf byte = 0x0A
)

func raceLanguage(race byte) byte {
	switch race {
	case raceOrc, raceUndead, raceTauren, raceTroll, raceBloodElf, raceGoblin:
		return 0x01
	default:
		return 0x07
	}
}
